def _train_step(
    self,
    step: int,
    batch: Dict[str, Any]
) -> Dict[str, Any]:
    model = self.module.train()
    model._pre_steps(step)

    loss, batch_log = model(batch)  ## here the loss is obtained, replace this loss with DPO, batch_log can be any dict
    loss.backward()
    self.train_op()

    return batch_log


